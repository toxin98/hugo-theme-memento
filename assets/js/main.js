console.log('This site was generated by Hugo.');

// menu
document.addEventListener("DOMContentLoaded", () => {
  const hamburger = document.getElementById('hamburger');
  hamburger.addEventListener("click", () => {
    document.getElementById("navBar").classList.toggle("show");
  });
});

// docs
document.addEventListener('DOMContentLoaded', () => {
  const body = document.body;
  const toggleLeft = document.querySelector('.toggle-left');
  const sidebarLeft = document.querySelector('.sidebar-left');

  if (!toggleLeft || !sidebarLeft) {
    return;
  }

  // 1. 点击 FAB 切换
  toggleLeft.addEventListener('click', (e) => {
    e.stopPropagation(); // 阻止冒泡
    body.classList.toggle('sidebar-open');
  });

  // 2. 点击外部关闭
  document.addEventListener('click', (e) => {
    // 如果没打开，直接返回
    if (!body.classList.contains('sidebar-open')) return;

    // 如果点击的不是 Sidebar，则关闭
    // 注意：因为 Header z-index 高，点击 Header 不会触发这里的遮罩层逻辑，
    // 但点击 Header 空白处通常也期望关闭菜单，这行代码能顺便覆盖此场景。
    if (!sidebarLeft.contains(e.target)) {
      body.classList.remove('sidebar-open');
    }
  });

});

document.addEventListener('DOMContentLoaded', () => {
  // posts
  document.querySelectorAll(".posts-category-title").forEach(button => {
    button.addEventListener("click", () => {

      document.querySelectorAll(".posts-category-title").forEach(b => {
        b.classList.remove("active");
      });
      button.classList.add("active");

      showYear(button.dataset.target);
    });
  });

  function showYear(year) {
    document.querySelectorAll(".posts-year-collection").forEach(el => {
        if (year === "all") {
            el.classList.add("show");
        } else {
            el.classList.toggle("show", el.classList.contains(year));
        }
    });
  }

});